CREATE TABLE "USERS_INFO" (
                         "id" long  PRIMARY KEY auto_increment,
                         "first_name" varchar,
                         "last_name" varchar,
                         "middle_name" varchar,
                         "passport_series" varchar,
                         "passport_number" varchar,
                         "create_date_user" timestamp
);

CREATE TABLE "USERS_DATE" (
                              "id" long  PRIMARY KEY auto_increment,
                              "first_name" varchar,
                              "last_name" varchar,
                              "middle_name" varchar,
                              "passport_series" varchar,
                              "passport_number" varchar,
                              "create_date_user" timestamp
);

